package com.Encapsulation;
 //NOTE : Mainly we use this Encapsulation for Data Security 
public class EncapsulationDemo {

	private int id;
	private String name;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
	
	
	
	
}
